# Arithmetic Operators and Strings

# Arithmetic operators:

age1 = 12
age2 = 18

print(age1 + age2) # sum
print(age1 - age2) # subtraction
print(age1 * age2) # multiplication
print(age1 / age2) # division
print(age1 % age2) # modulas

# Strings

sent1 = "today is a beautiful day"

print(sent1)

first_name = "Nazmus"
last_name = "Sakib"
print(first_name + " " + last_name) # Concatenation

# subtraction multiplication division modulas not applicable on strings

print("hi" * 10); 

# Sub string/slicing

print(sent1[0:5])